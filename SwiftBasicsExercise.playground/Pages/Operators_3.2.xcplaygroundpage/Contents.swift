//MARK: Create one example for show usage of operator's addition, multiplication, division for integers and floats
//For Integers
var numInt1 = 15
var numInt2 = 2
print("\(numInt1) + \(numInt2) = \(numInt1 + numInt2)")
print("\(numInt1) - \(numInt2) = \(numInt1 - numInt2)")
print("\(numInt1) * \(numInt2) = \(numInt1 * numInt2)")
print("\(numInt1) / \(numInt2) = \(numInt1 / numInt2)")
print("\(numInt1) % \(numInt2) = \(numInt1 % numInt2)")

//For Floats
var numFloat1 = 15.2
var numFloat2 = 2.4
print("\(numFloat1) + \(numFloat2) = \(numFloat1 + numFloat2)")
print("\(numFloat1) - \(numFloat2) = \(numFloat1 - numFloat2)")
print("\(numFloat1) * \(numFloat2) = \(numFloat1 * numFloat2)")
print("\(numFloat1) / \(numFloat2) = \(numFloat1 / numFloat2)")

